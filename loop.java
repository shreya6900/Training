package com.loop;

public class loop
{

	public static void main(String[] args) 
	{
		for(int i=0;i<=100;i++)
		{
			if(i%2==0)
			{
				System.out.print(" "+i);
			}
		}
	   int j=0;
	   while(j<=100)
	   {
		   if(j%2!=0)
		   {
			   System.out.print(" "+j); 
			   j++;
		   }
		   
	   }
	}
}
		
	